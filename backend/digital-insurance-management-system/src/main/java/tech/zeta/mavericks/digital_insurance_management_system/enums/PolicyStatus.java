package tech.zeta.mavericks.digital_insurance_management_system.enums;

/**
 * Enum representing the current status of a user policy.
 *
 * ACTIVE        : Policy is currently active
 * EXPIRED       : Policy has expired
 * RENEWED       : Policy has been renewed
 * CANCELLED     : Policy has been cancelled
 * RENEW_PENDING : Policy renewal is pending
 */
public enum PolicyStatus {
    ACTIVE,
    EXPIRED,
    RENEWED,
    CANCELLED,
    RENEW_PENDING
}
