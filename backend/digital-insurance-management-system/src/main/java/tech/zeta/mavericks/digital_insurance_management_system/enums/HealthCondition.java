package tech.zeta.mavericks.digital_insurance_management_system.enums;

/**
 * Enum representing various preexisting health conditions of a user.
 *
 * Used in health-related policies to determine premiums or eligibility.
 *
 * CANCER     : User has a history of cancer
 * DIABETES   : User has diabetes
 * TB         : User has tuberculosis
 * BP         : User has blood pressure issues
 */
public enum HealthCondition {
    CANCER,
    DIABETES,
    TB,
    BP
}
