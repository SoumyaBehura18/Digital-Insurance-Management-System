package tech.zeta.mavericks.digital_insurance_management_system.enums;

/**
 * Enum representing the status of a message in the support ticket system.
 *
 * SENT     : Message has been sent by the user or admin
 * EDITED   : Message has been edited after being sent
 * DELETED  : Message has been deleted
 */
public enum MessageStatus {
        SENT,
        EDITED,
        DELETED
}
