/**
 * Enum representing the status of a support ticket.
 *
 * OPEN     : Ticket has been created and is awaiting resolution
 * RESOLVED : Ticket issue has been resolved
 * CLOSED   : Ticket has been closed
 */
public enum TicketStatus {
    OPEN,
    RESOLVED,
    CLOSED
}
